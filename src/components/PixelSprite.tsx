"use client";

export default function PixelSprite({
  sprite,
  size = 64,
  className = "",
  animate = true,
}: {
  sprite: string[][];
  size?: number;
  className?: string;
  animate?: boolean;
}) {
  const pixelSize = size / sprite[0].length;

  return (
    <div
      className={`inline-block ${className}`}
      style={{
        width: size,
        height: (sprite.length / sprite[0].length) * size,
        animation: animate ? "float 3s ease-in-out infinite" : undefined,
      }}
    >
      <svg
        width={size}
        height={(sprite.length / sprite[0].length) * size}
        viewBox={`0 0 ${sprite[0].length} ${sprite.length}`}
        style={{ imageRendering: "pixelated" }}
      >
        {sprite.map((row, y) =>
          row.map((color, x) =>
            color !== "." ? (
              <rect
                key={`${x}-${y}`}
                x={x}
                y={y}
                width={1}
                height={1}
                fill={color}
              />
            ) : null
          )
        )}
      </svg>
    </div>
  );
}

